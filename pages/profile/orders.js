import Head from 'next/head'
import Image from 'next/image'
import Link from 'next/link'
import styles from 'styles/profile/orders.module.css'
import AuthenticatedRoute from '@/components/authenticatedRoute'
import Order from '@/components/Order'

import nookies from 'nookies'
import Cookie from 'cookie-cutter'

import {useState, useEffect} from 'react'

export default function Orders({user}) {

  const [show, setShow] = useState(false)
  const [current, setCurrent] = useState({})


  const [orders, setOrders] = useState([])

  const showElement = (item)=>{
    setShow(true)
    setCurrent(item)
  }

  const hideElement = () => {
    setShow(false)
    setCurrent({})
  }

  useEffect(()=>{

    const userAccess = Cookie.get('accessToken')

    const fetchStats = async (token)=>{
      await fetch(`http://localhost:8000/user/get-orders`, {
        method:'GET',
        headers:{
          'Content-Type':'application/json',
          'Access-Control-Allow-Origin':'cors',
          'authorization': `Bearer ${token}`
        },
        mode:'cors'
      }).then(async res=>await res.json()).then(data =>{
        setOrders(data)
      })
    }

    fetchStats(userAccess)

  },[])


  return (
    <AuthenticatedRoute>
      <div>
        <Head>
          <title>Profile</title>
          <meta name="description" content="Generated by create next app" />
          <link rel="icon" href="/favicon.ico" />
        </Head>
        <main className={!show ? styles.container : [styles.container, styles.show].join(" ")}>
          <div className={styles.list}>         
            <p>Orders</p>
            <span>All completed orders</span>
            {orders.map((order)=>(
              <div className={styles.order} onClick={()=>{
                showElement(order)
              }}>
                <div className={styles.order_images}>
                  {order.items.map((item,idx)=>(
                    <div className={styles.img_container} style={{left:idx==0 ? 0 : (100/order.items.length)*idx+'%' }}>
                      <img src={item.image}/>
                    </div>
                  ))}
                </div>
                <div className={styles.order_details}>
                  {order._id}
                  {order.subtotal}
                </div>
              </div>
            ))

            }
          </div> 
          <div className={styles.details}>
            {Object.keys(current).length > 0 &&
              <Order toggle={hideElement} order={current}/>
            }
          </div>
        </main>
      </div>
    </AuthenticatedRoute>
  )
}

export async function getServerSideProps(context){

  const cookies = nookies.get(context)

  console.log({cookies})

  if(!cookies.accessToken) {
    return {
      redirect:{
        permanent:false,
        destination:'/signin'
      }
    }
  }

  return{
    props:{
      cookies
    }
  }
}